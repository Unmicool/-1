salary = 5000  # Ежемесячная зарплата
spend = 6000  # Траты за первый месяц
months = 10  # Количество месяцев
increase = 0.03  # Ежемесячный рост цен

# Инициализируем переменную для расчета необходимой подушки безопасности
money_capital = 0  # Начальная подушка безопасности
current_spend = spend  # Расходы за первый месяц

# Подсчет необходимой суммы подушки безопасности
for month in range(months):
    # Считаем нехватку средств для текущего месяца
    shortage = current_spend - salary
    # Если зарплаты недостаточно, добавляем нехватку к подушке безопасности
    if shortage > 0:
        money_capital += shortage
    # Увеличиваем расходы на следующий месяц с учетом роста цен
    current_spend *= (1 + increase)

# Округляем результат до целого числа
money_capital = round(money_capital)

print(f"Подушка безопасности, чтобы протянуть {months} месяцев без долгов:", money_capital)